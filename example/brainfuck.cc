#include <cstdio>
#include <cstring>
#include <string>
#include <unordered_map>

std::unordered_map<int, int> preprocess(const std::string& source) {
  int stack[1024];
  memset(stack, 0, sizeof(stack));
  int *sp = stack;
  std::unordered_map<int, int> jumps;
  for(int pc = 0; pc < source.length(); pc++) {
    if(source[pc] == '[') {
      *sp++ = pc;
    } else if(source[pc] == ']') {
      int target = *--sp;
      jumps[target] = pc;
      jumps[pc] = target;
    }
  }
  return jumps;
}

void execute(const std::string& source) {
  char data[1024];
  memset(data, 0, sizeof(data));
  char* ptr = data;
  int pc = 0;
  auto jumps = preprocess(source);
  while(pc < source.length()) {
    char c = source[pc];
    if(c == '>') {
      ptr++;
    } else if(c == '<') {
      ptr--;
    } else if(c == '+') {
      (*ptr)++;
    } else if(c == '-') {
      (*ptr)--;
    } else if(c == '.') {
      putchar(*ptr);
    } else if(c == ',') {
      *ptr = getchar();
    } else if(c == '[') {
      if(*ptr == 0) {
        pc = jumps[pc];
        continue;
      }
    } else if(c == ']') {
      if(*ptr != 0) {
        pc = jumps[pc];
        continue;
      }
    }
    pc++;
  }
}

