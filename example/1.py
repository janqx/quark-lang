def precomputeJumps(code):
  stack = [0 for _ in range(100000)]
  jumps = [0 for _ in range(100000)]
  sp = 0
  for pc in range(len(code)):
    if code[pc] == '[':
      stack[sp] = pc
      sp += 1
    elif code[pc] == ']':
      sp -= 1
      target = stack[sp]
      jumps[target] = pc
      jumps[pc] = target
  return jumps

def execute(code):
  data = [0 for _ in range(100000)]
  pc = 0
  i = 0
  jumps = precomputeJumps(code)

  while pc < len(code):
    c = code[pc]
    if c == '>':
      i += 1
    elif c == '<':
      i -= 1
    elif c == '+':
      data[i] += 1
    elif c == '-':
      data[i] -= 1
    elif c == '.':
      print(chr(data[i]), end='')
    elif c == ',':
      data[i] = input("")
    elif c == '[':
      if data[i] == 0:
        pc = jumps[pc]
        continue
    elif c == ']':
      if data[i] != 0:
        pc = jumps[pc]
        continue
    pc += 1


